def isConv(QR,R):
    n = len(QR)
    randomInt = np.random.randint(0,n-1)
    if ((R[randomInt][randomInt]-QR[randomInt][randomInt])/QR[randomInt][randomInt] < 0.1):
        return True
    else:
        return False
    

